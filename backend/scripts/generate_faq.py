def _normalize(text: str) -> str:
    """카테고리 문자열 정규화 (매칭 정확도 향상)"""
    s = text.lstrip("\ufeff").strip()
    # 후행 대시/하이픈 + 표준구매 프로세스 제거
    s = re.sub(r'\s*[-–—]\s*(표준구매\s*프로세스|표준\s*구매\s*프로세스)?\s*$', '', s)
    # 점(.)을 가운뎃점(·)으로 통일
    s = s.replace('.', '·')
    # 괄호 내용 제거 (영문 포함) — "수선 공사(소방·전기·안전 등)" → "수선 공사"
    s = re.sub(r'\s*\([^)]*\)', '', s)
    # 다중 공백 정리
    s = re.sub(r'\s+', ' ', s)
    return s.strip()


def get_taxonomy(chunk_category: str) -> tuple[str, str]:
    """chunk 카테고리 → (대분류, 중분류) 변환"""
    # 정확 매칭
    if chunk_category in CATEGORY_TAXONOMY_MAP:
        return CATEGORY_TAXONOMY_MAP[chunk_category]

    # 정규화 후 재시도
    clean = _normalize(chunk_category)
    if clean in CATEGORY_TAXONOMY_MAP:
        return CATEGORY_TAXONOMY_MAP[clean]

    # 부분 매칭 시도 (정규화된 키 비교)
    for key, val in CATEGORY_TAXONOMY_MAP.items():
        norm_key = _normalize(key)
        if norm_key in clean or clean in norm_key:
            return val

    print(f"  [!] 매핑 없음: '{chunk_category}' → 기타")
    return ("기타", "기타")


