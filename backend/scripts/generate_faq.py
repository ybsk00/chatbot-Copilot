def generate_faq_from_chunk(content: str, category: str, doc_name: str) -> list[dict]:
    """flash-lite로 청크에서 Q&A 쌍 생성"""
    prompt = FAQ_PROMPT.format(
        category=category,
        doc_name=doc_name,
        content=content,
    )

    response = _client.models.generate_content(
        model=MODELS["refinement"],
        contents=prompt,
        config={"max_output_tokens": 2048},
    )

    text = response.text.strip()

    # JSON 파싱 (```json ... ``` 형식 처리)
    if text.startswith("```"):
        text = re.sub(r"```json?\s*", "", text)
        text = re.sub(r"```\s*$", "", text)

    faqs = json.loads(text)
    if not isinstance(faqs, list):
        faqs = [faqs]

    # 길이 검증
    valid = []
    for faq in faqs:
        q = faq.get("question", "").strip()
        a = faq.get("answer", "").strip()
        if q and a and len(a) >= 50:
            valid.append({"question": q, "answer": a})

    return valid


