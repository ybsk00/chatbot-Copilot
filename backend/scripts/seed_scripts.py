def main():
    sb = get_client()

    # 기존 데이터 확인
    existing = sb.table("conversation_scripts").select("id").execute()
    if existing.data:
        print(f"[!] 기존 {len(existing.data)}건 삭제 후 재시드")
        sb.table("conversation_scripts").delete().neq("id", 0).execute()

    total = len(SCRIPTS)
    print(f"[*] {total}개 화법 스크립트 시드 시작...")

    for i, s in enumerate(SCRIPTS):
        # 임베딩 생성
        emb = embed_query(s["content"])

        # DB 삽입
        sb.table("conversation_scripts").insert({
            "category": s["category"],
            "cta_phase": s["cta_phase"],
            "script_type": "style",
            "content": s["content"],
            "embedding": emb,
            "is_active": True,
        }).execute()

        print(f"  [{i+1}/{total}] {s['category']} / {s['cta_phase']}")

        # Rate limit (Gemini embedding)
        if (i + 1) % 10 == 0:
            time.sleep(1)

    print(f"\n[OK] {total}개 화법 스크립트 시드 완료!")


if __name__ == "__main__":
    main()
