/**
 * 공통 필드 객체 생성 (카테고리별)
 * @param {string} category - PR 카테고리 키
 * @returns {Object} c1~c20 필드 객체
 */
export function buildCommonFields(category) {
  const defaults = COMMON_DEFAULTS[category] || COMMON_DEFAULTS._generic;
  const fields = {};
  for (const [key, label] of Object.entries(COMMON_FIELD_LABELS)) {
    fields[key] = {
      label,
      value: "",
      required: COMMON_REQUIRED[key] ?? false,
      ...(defaults[key] !== undefined && defaults[key] !== "" ? { default: defaults[key] } : {}),
    };
  }
  return fields;
}

/**
 * 공통 섹션 배열 생성 (고유 섹션 삽입 위치 지정)
 * @param {Array} specificSections - 카테고리 고유 섹션 배열
 * @returns {Array} 전체 섹션 배열 (공통 + 고유 통합)
 */
export function buildSections(specificSections) {
  const paymentSection = {
    ...COMMON_SECTIONS.payment,
    title: `${specificSections.length + 4}. 결제·계약 조건`,
  };
  return [
    COMMON_SECTIONS.requester,
    COMMON_SECTIONS.contract,
    COMMON_SECTIONS.require,
    ...specificSections.map((s, i) => ({
      ...s,
      title: `${i + 4}. ${s.title}`,
    })),
    paymentSection,
  ];
}
